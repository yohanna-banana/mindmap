<node TEXT="How are keys generated?" ID="ID_1959525119" CREATED="1755756866017" MODIFIED="1755757071714" HGAP_QUANTITY="-10.75 pt">
<node ID="ID_323053844" CREATED="1755756908571" MODIFIED="1755757226572"><richcontent TYPE="NODE">

<html>
  <head>
    
  </head>
  <body>
    <p>
      During cryptocurrency transactions, there are two keys used
    </p>
    <p>
      &#xa0;- a public and a private key
    </p>
    <p>
      
    </p>
    <p>
      During a cryptocurrency transaction, the public key is generated alongside the private key, where the public key is the <span style="font-style: italic;">wallet address </span>of both the sender and receiver, and the private key is a “<span style="font-weight: bold;">long string similar to a password, where anyone can access the funds from the associated wallet address</span>” (Brex., n.d.). The private key generates up to “64 digits in a hexadecimal format. It acts like an <span style="font-style: italic;">access code</span>&#xa0;and <span style="font-style: italic;">proves ownership </span>of an asset on the blockchain” (Investopedia(a)., 2024).
    </p>
    <p>
      
    </p>
    <p>
      Because of what each key represents or does, the public key can be shared openly with anyone without consequence, but the private key should never be shared. Sharing a private key can lead to theft or loss of crypto. When a public key is generated, it just looks like a long string of letters and numbers, but it will never be possible for a threat actor to restore the original set of characters. The long string of numbers is generated based on the ciphertext values of, in this case, the wallet address. These strings can be further hashed using cryptographic algorithms like SHA-256 or MD5 for an added layer of security. An example of what a public key would look like is:
    </p>
    <p>
      
    </p>
    <p>
      ‘02c1b7e3e6e7af5f3bcd9cf9a58f9f27d7c6a2ab1c0d3b7a7d8e6b2f0c5e9a0a1b’
    </p>
  </body>
